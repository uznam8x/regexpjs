import match from "../match";
export default function (str: string): string[] {
  const list = [
    {
      key: "American Express",
      regex: /^3[47][0-9]{13}$/,
    },
    {
      key: "BCGlobal",
      regex: /^(6541|6556)[0-9]{12}$/,
    },
    {
      key: "Carte Blanche",
      regex: /^389[0-9]{11}$/,
    },
    {
      key: "Diners Club",
      regex: /^3(?:0[0-5]|[68][0-9])[0-9]{11}$/,
    },
    {
      key: "Discover",
      regex:
        /^65[4-9][0-9]{13}|64[4-9][0-9]{13}|6011[0-9]{12}|(622(?:12[6-9]|1[3-9][0-9]|[2-8][0-9][0-9]|9[01][0-9]|92[0-5])[0-9]{10})$/,
    },
    {
      key: "Insta Payment",
      regex: /^63[7-9][0-9]{13}$/,
    },
    {
      key: "JCB",
      regex: /^(?:2131|1800|35\d{3})\d{11}$/,
    },
    {
      key: "Korean Local",
      regex: /^9[0-9]{15}$/,
    },
    {
      key: "Laser",
      regex: /^(6304|6706|6709|6771)[0-9]{12,15}$/,
    },
    {
      key: "Maestro",
      regex: /^(5018|5020|5038|6304|6759|6761|6763)[0-9]{8,15}$/,
    },
    {
      key: "Master",
      regex:
        /^(5[1-5][0-9]{14}|2(22[1-9][0-9]{12}|2[3-9][0-9]{13}|[3-6][0-9]{14}|7[0-1][0-9]{13}|720[0-9]{12}))$/,
    },
    {
      key: "Solo",
      regex: /^(6334|6767)[0-9]{12}|(6334|6767)[0-9]{14}|(6334|6767)[0-9]{15}$/,
    },
    {
      key: "Switch",
      regex:
        /^(4903|4905|4911|4936|6333|6759)[0-9]{12}|(4903|4905|4911|4936|6333|6759)[0-9]{14}|(4903|4905|4911|4936|6333|6759)[0-9]{15}|564182[0-9]{10}|564182[0-9]{12}|564182[0-9]{13}|633110[0-9]{10}|633110[0-9]{12}|633110[0-9]{13}$/,
    },
    {
      key: "Union Pay",
      regex: /^(62[0-9]{14,17})$/,
    },
    {
      key: "Visa",
      regex: /^4[0-9]{12}(?:[0-9]{3})?$/,
    },
  ];

  let res: any[] = [];

  for (let i = 0, len = list.length; i < len; i++) {
    const ans = match(list[i].regex, str);
    if (ans.length) res = res.concat([list[i].key]);
  }

  return res;
}
